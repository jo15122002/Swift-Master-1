// MARK: - APIResponse
struct APIResponse: Codable {
    let content: String
    let generationSettings: GenerationSettings
    let model, prompt: String
    let slotID: Int
    let stop, stoppedEOS, stoppedLimit, stoppedWord: Bool
    let stoppingWord: String
    let timings: Timings
    let tokensCached, tokensEvaluated, tokensPredicted: Int
    let truncated: Bool

    enum CodingKeys: String, CodingKey {
        case content
        case generationSettings = "generation_settings"
        case model, prompt
        case slotID = "slot_id"
        case stop
        case stoppedEOS = "stopped_eos"
        case stoppedLimit = "stopped_limit"
        case stoppedWord = "stopped_word"
        case stoppingWord = "stopping_word"
        case timings
        case tokensCached = "tokens_cached"
        case tokensEvaluated = "tokens_evaluated"
        case tokensPredicted = "tokens_predicted"
        case truncated
    }
}

